/* 写1个字节的指令或数据到PCD8544 */
void Lcd_Pcd8544_WriteByte(uint8_t WriteByte, uint8_t CommandOrDataSelect)
{
	uint8_t i;
	Lcd_Pcd8544_SCE_L(); //开芯片使能，0有效
	
	if(CommandOrDataSelect == 1)
	{
		Lcd_Pcd8544_DC_H();	// 指令(D/C = 0), RAM数据(D/C = 1)
	}
	else Lcd_Pcd8544_DC_L();
	
	for(i = 0; i < 8; i++)
	{
		if(WriteByte & 0x80)
		{
			Lcd_Pcd8544_SDIN_H();
		}
		else Lcd_Pcd8544_SDIN_L();
		
		Lcd_Pcd8544_SCLK_L();
		WriteByte <<= 1;
		Lcd_Pcd8544_SCLK_H();
	}
	Lcd_Pcd8544_SCE_H();	//关芯片使能，0有效
}

/* 延时子程序 */
void Lcd_Pcd8544_Delayms(uint32_t n)
{
	uint32_t j,k;
	for(j = 0; j < n; j++)
		for(k = 0; k < 1000; k++);
}

/* PCD8544初始化 */
void Lcd_Pcd8544_Init(void)
{
	Lcd_Pcd8544_RST_H();	// RST = 0	
	Lcd_Pcd8544_Delayms(100);
	Lcd_Pcd8544_RST_H();	// RST = 1
	
	Lcd_Pcd8544_WriteByte(0x21, 0);		//打开扩展指令集设置LCD模式
	Lcd_Pcd8544_WriteByte(0xCC, 0);		//设置偏值电压
	Lcd_Pcd8544_WriteByte(0x06, 0);		//温度校正
	Lcd_Pcd8544_WriteByte(0x13, 0);		//1；48
	Lcd_Pcd8544_WriteByte(0x20, 0);		//选择基本指令集
	Lcd_Pcd8544_ClrScreen();
	Lcd_Pcd8544_WriteByte(0x0C, 0);		//设置显示配置为普通模式
}

/* 清屏 */
void Lcd_Pcd8544_ClrScreen(void)
{
	uint16_t n;
	Lcd_Pcd8544_WriteByte(0x0C, 0);		//设置显示配置为普通模式
	Lcd_Pcd8544_WriteByte(0x80, 0);
	for(n = 0; n < 504; n++)
	{
		Lcd_Pcd8544_WriteByte(0x00, 1); //写504个8位数据0，将像素全部置0
	}
}

/* 设置显示坐标 */
void Lcd_Pcd8544_SetPos(uint16_t Xpos, uint16_t Ypos)
{
  Lcd_Pcd8544_WriteByte(0x80 | Xpos, 0);
	Lcd_Pcd8544_WriteByte(0x40 | Ypos, 0);
}

/* 显示英文字符 */
void Lcd_Pcd8544_PutEnglishChar(uint8_t EnglishChar)
{
	uint8_t line;
	EnglishChar = EnglishChar - 32;
	for(line = 0; line < 6; line++)
	{
		Lcd_Pcd8544_WriteByte(font6x8[EnglishChar][line], 1);
	}
}

/* 显示英文字符串 */
void Lcd_Pcd8544_PutEnglishString(uint16_t X, uint16_t Y, uint8_t *EnglishString)
{
	Lcd_Pcd8544_SetPos(X, Y);
	while(*EnglishString)
	{
		Lcd_Pcd8544_PutEnglishChar(*EnglishString);
		EnglishString++;
	}
	
}


